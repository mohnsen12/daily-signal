#!/usr/bin/env node
/**
 * The Daily Signal — Launch Day Dashboard
 * 
 * Reads analytics data and generates a live dashboard report.
 * Usage: node scripts/launch-dashboard.mjs [--json]
 * 
 * For real-time data, point to the deployed site's localStorage export.
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ANALYTICS_FILE = path.join(__dirname, '..', 'landing-page', 'data', 'analytics-launch.json');
const REPORT_FILE = path.join(__dirname, '..', 'content', 'LAUNCH_DASHBOARD.md');

// Try to load analytics data
let data = { events: [], sessions: {} };
try {
  if (fs.existsSync(ANALYTICS_FILE)) {
    data = JSON.parse(fs.readFileSync(ANALYTICS_FILE, 'utf8'));
  }
} catch(e) {
  console.log('⚠️  No analytics data found. Create landing-page/data/analytics-launch.json');
}

const events = data.events || [];
const sessions = data.sessions || {};

// Compute stats
const totalEvents = events.length;
const totalSessions = Object.keys(sessions).length;
const pageviews = events.filter(e => e.event === 'pageview').length;
const signups = events.filter(e => e.event === 'signup').length;
const phClicks = events.filter(e => e.event === 'ph_click').length;
const twitterClicks = events.filter(e => e.event === 'twitter_click').length;

// Traffic sources
const sources = {};
for (const [sid, s] of Object.entries(sessions)) {
  const src = s.utm?.source || 'unknown';
  sources[src] = (sources[src] || 0) + 1;
}

// Scroll depth distribution
const scrollDepths = events.filter(e => e.event === 'scroll_depth');
const avgScroll = scrollDepths.length > 0 
  ? Math.round(scrollDepths.reduce((a, e) => a + (e.depth || 0), 0) / scrollDepths.length)
  : 0;

// Time on page
const timeEvents = events.filter(e => e.event === 'time_on_page');
const avgTime = timeEvents.length > 0
  ? Math.round(timeEvents.reduce((a, e) => a + (e.seconds || 0), 0) / timeEvents.length)
  : 0;

// Hourly distribution
const hourly = {};
for (const e of events) {
  if (e.event === 'pageview') {
    const hour = new Date(e.timestamp).getUTCHours();
    hourly[hour] = (hourly[hour] || 0) + 1;
  }
}

// Conversion rate
const convRate = pageviews > 0 ? ((signups / pageviews) * 100).toFixed(1) : '0.0';

// Generate report
const now = new Date().toISOString().replace('T', ' ').substring(0, 19);
const report = `# 📊 Launch Day Dashboard

**Opdateret:** ${now} UTC  
**Dato:** 18. marts 2026 (Product Hunt Launch Day)

---

## 📈 Key Metrics

| Metric | Value |
|--------|-------|
| 👁️ Page Views | ${pageviews} |
| 👥 Sessions | ${totalSessions} |
| ✉️ Signups | ${signups} |
| 📊 Conversion Rate | ${convRate}% |
| 🔗 PH Clicks | ${phClicks} |
| 🐦 Twitter Clicks | ${twitterClicks} |
| 📜 Avg Scroll Depth | ${avgScroll}% |
| ⏱️ Avg Time on Page | ${avgTime}s |

---

## 🌍 Traffic Sources

${Object.entries(sources).sort((a, b) => b[1] - a[1]).map(([src, count]) => {
  const pct = totalSessions > 0 ? ((count / totalSessions) * 100).toFixed(0) : 0;
  const emoji = {
    'producthunt': '🚀', 'reddit': '🤖', 'twitter': '🐦', 'linkedin': '💼',
    'hackernews': '🟠', 'google': '🔍', 'direct': '🔗', 'github': '🐱'
  }[src] || '🌐';
  return `- ${emoji} **${src}**: ${count} sessions (${pct}%)`;
}).join('\n') || '- Ingen data endnu'}

---

## ⏰ Hourly Traffic (UTC)

${Object.entries(hourly).sort((a, b) => a[0] - b[0]).map(([hour, count]) => {
  const bar = '█'.repeat(Math.min(count, 40));
  return `- \`${hour.padStart(2, '0')}:00\` ${bar} ${count}`;
}).join('\n') || '- Ingen data endnu'}

---

## 🎯 Launch Day Targets

| Target | Goal | Current | Status |
|--------|------|---------|--------|
| PH Upvotes | 100+ | — | ⏳ |
| Website Visits | 500+ | ${pageviews} | ${pageviews >= 500 ? '✅' : pageviews >= 100 ? '🟡' : '🔴'} |
| New Subscribers | 30+ | ${signups} | ${signups >= 30 ? '✅' : signups >= 10 ? '🟡' : '🔴'} |
| Conversion Rate | >5% | ${convRate}% | ${parseFloat(convRate) >= 5 ? '✅' : parseFloat(convRate) >= 2 ? '🟡' : '🔴'} |

---

## 📱 Session Details

${Object.entries(sessions).slice(-10).map(([sid, s]) => {
  const time = s.firstSeen ? new Date(s.firstSeen).toISOString().replace('T', ' ').substring(11, 19) : '?';
  const src = s.utm?.source || 'unknown';
  const camp = s.utm?.campaign !== 'none' ? ` (${s.utm.campaign})` : '';
  return `- \`${time}\` ${src}${camp} — ${s.language || '?'} ${s.screen || ''}`;
}).join('\n') || '- Ingen sessions endnu'}

---

*Auto-generated by Teddy 🐕*
`;

fs.writeFileSync(REPORT_FILE, report);
console.log(report);

// Also output JSON if requested
if (process.argv.includes('--json')) {
  console.log('\n--- RAW JSON ---');
  console.log(JSON.stringify(data, null, 2));
}
